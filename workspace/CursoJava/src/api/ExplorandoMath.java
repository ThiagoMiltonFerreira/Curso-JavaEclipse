package api;

public class ExplorandoMath {

	public static void main(String[] args) {
	
		System.out.println(Math.PI);  // retorna o PI
		System.out.println(Math.pow(2, 3)); //potenciaçao
		System.out.println(Math.abs(-10)); // retona o valor absoluto ou seja positivo
		System.out.println(Math.min(70, 7)); // mosta o menor valor
		System.out.println(Math.max(70, 7)); // mosta o maior valor
		
		System.out.println(Math.ceil(6.55)); // arredonda para cima
		System.out.println(Math.floor(6.55)); // arredonda para baixo
		System.out.println(Math.random()); // valor randomico entre 0 e 1
		
		
		
		
		
	}

}
